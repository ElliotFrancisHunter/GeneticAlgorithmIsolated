/// <summary>
/// Public method that calls the constituent methods required to run the algorithm.
/// Also provides visual feedback on total fitness per generation and the coordinates of each point at the final generation.
/// Prints each stage of the algorithm to make better use of debugging.
/// </summary>
void GANodePlacement::RunGeneticAlgorithm()
{
    GeneratePopulation();
    while (mGenerationCount < 10000)
    {
        std::cout << "Running Generation: " << mGenerationCount << std::endl;
        mTotalFitness = 0;

        std::cout << "Running Fitness Function: " << mGenerationCount << std::endl;
        FitnessFunction();

        std::cout << "Running Selection: " << mGenerationCount << std::endl;
        TournamentSelection();

        std::cout << "Running Crossover: " << mGenerationCount << std::endl;
        CrossoverParents();
        ++mGenerationCount;

        std::for_each(mMapPopulation.begin(), mMapPopulation.end(), [&](Chromosome chromo) {
            mTotalFitness += chromo.mFitnessScore;
            });
        std::cout << "Generation: " << mGenerationCount << "\t Total Fitness: " << mTotalFitness << std::endl;
    }
    std::for_each(mMapPopulation.begin(), mMapPopulation.end(), [](const Chromosome& chromo) {
        std::cout << "X: " << chromo.mPositions.first << "\t Y: " << chromo.mPositions.second << std::endl;
        });
}

