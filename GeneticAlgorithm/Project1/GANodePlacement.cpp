/// <summary>
/// This method accumulates the 'fitness' of a given Chromosome(Our x,y coordinate). 
/// It iterates throught the population and awards points given certain criteria are met, and subtracts points given other conditions are met.
/// Currently this is weighted strongly against overlapping points with a preference towards ones about 3 points apart from each other, with 4 points or greater being less rewarded. 
/// If too far away from a given point, a mild debuff is supplied.
/// This is to simulate connections as we want them to be close.
void GANodePlacement::FitnessFunction()
{
    // Reset population ready for recalculation of fitness and later competition in selection stage
    for (auto& chromo : mMapPopulation)
    {
        chromo.mFitnessScore = 0;
        chromo.bCompeting = false;
    }

    // Nested loop to compare every member of the population to each other without comparing an individual to themselves
    for (auto it1 = 0; it1 != mMapPopulation.size(); ++it1)
    {
        for (auto it2 = 1; it2 != mMapPopulation.size(); ++it2)
        {
            // Overlap check - EH
            if (abs(mMapPopulation[it1].mPositions.first - mMapPopulation[it2].mPositions.first) <= 32
                || abs(mMapPopulation[it1].mPositions.second - mMapPopulation[it2].mPositions.second) <= 32)
            {
                mMapPopulation[it1].mFitnessScore += mUnfitModifier;
            }
            // Short distance check
            if ((abs(mMapPopulation[it1].mPositions.first - mMapPopulation[it2].mPositions.first >= 60 && mMapPopulation[it1].mPositions.first - mMapPopulation[it2].mPositions.first) < 90)
                || abs(mMapPopulation[it1].mPositions.second - mMapPopulation[it2].mPositions.second >= 60 && mMapPopulation[it1].mPositions.second - mMapPopulation[it2].mPositions.second) < 90)
            {
                mMapPopulation[it1].mFitnessScore += mGreatFitnessModifier;
            }
            // Medium distance check
            else if ((abs(mMapPopulation[it1].mPositions.first - mMapPopulation[it2].mPositions.first >= 90 && mMapPopulation[it1].mPositions.first - mMapPopulation[it2].mPositions.first) < 120)
                || abs(mMapPopulation[it1].mPositions.second - mMapPopulation[it2].mPositions.second >= 90 && mMapPopulation[it1].mPositions.second - mMapPopulation[it2].mPositions.second) < 120)
            {
                mMapPopulation[it1].mFitnessScore += mBestFitnessModifier;
            }
            // Long distance check
            else if ((abs(mMapPopulation[it1].mPositions.first - mMapPopulation[it2].mPositions.first >= 120 && mMapPopulation[it1].mPositions.first - mMapPopulation[it2].mPositions.first) < 150)
                || abs(mMapPopulation[it1].mPositions.second - mMapPopulation[it2].mPositions.second >= 120 && mMapPopulation[it1].mPositions.second - mMapPopulation[it2].mPositions.second) < 150)
            {
                mMapPopulation[it1].mFitnessScore += mGoodFitnessModifier;
            }
            // Too far away debuff
            else
            {
                if ((abs(mMapPopulation[it1].mPositions.first - mMapPopulation[it2].mPositions.first) >= 150)
                    || abs(mMapPopulation[it1].mPositions.second - mMapPopulation[it2].mPositions.second) >= 150)
                {
                    mMapPopulation[it1].mFitnessScore += mLessFitModifier;
                }
            }
        }
    }

}

/// <summary>
/// Randomly selects a proportion of the population and places the individual of each contest in the mParents container (this is Elitist preference - strongest always wins).
/// </summary>
void GANodePlacement::TournamentSelection()
{

    POPULATION sortedContestants;

    while (mParents.size() < 2)
    {
        while (sortedContestants.size() < mMapPopulation.size() / 5)
        {
            int num = RandomRange(0, mMapPopulation.size() - 1);
            if (mMapPopulation[num].bCompeting == false)
            {
                mMapPopulation[num].bCompeting = true;
                sortedContestants.push_back(mMapPopulation[num]);
            }            
        }

        // Sort into ascending order and copy to a vector for element access.
        std::sort(sortedContestants.begin(), sortedContestants.end(), [](Chromosome a, Chromosome b) { return a.mFitnessScore > b.mFitnessScore; });
        
        // Retrieves the 'Winner' and places them into the parent vector for crossover
        mParents.push_back(sortedContestants[0]);
        
        sortedContestants.clear();
    }     
}

/// <summary>
/// Crosses the position gene of the parent Chromosomes together to create a new Chromosome which is then inserted into the population
/// Makes use of whole arithmetic recombination method
/// </summary>
void GANodePlacement::CrossoverParents()
{  
     Chromosome child, mum, dad;
     mum = mParents[0];
     dad = mParents[1];
    
    // In this case, crossover is an average of the two parents positions - This is an example of whole arithmetic recombination
    child.mPositions.first = (mum.mPositions.first + dad.mPositions.first) / 2;
    child.mPositions.second = (mum.mPositions.second + dad.mPositions.second) / 2;
    
    // Potential for add some variety to child
    Mutate(child);
    
    mParents.clear();
    
    
    POPULATION sortingPop;
    std::copy(mMapPopulation.begin(), mMapPopulation.end(), std::back_inserter(sortingPop));
    
    while (!sortingPop.empty())
    {
        // Sort in ascending order (lowest fitness first)
        std::sort(sortingPop.begin(), sortingPop.end(), [](Chromosome a, Chromosome b) { return a.mFitnessScore < b.mFitnessScore; });
    
        // Only have weakest individual remaining 
        while (sortingPop.size() > 1)
        {
            sortingPop.pop_back();
        }
    
        // Replace one of the weakest points with one of the children
        auto it = std::find_if(mMapPopulation.begin(), mMapPopulation.end(), [&sortingPop](Chromosome& noChromo) {
            return (noChromo.mFitnessScore == sortingPop[0].mFitnessScore && noChromo.mPositions == sortingPop[0].mPositions);
            });
       
        mMapPopulation[it - mMapPopulation.begin()] = child;
        sortingPop.clear();
    }
}

/// <summary>
/// Method that is run on every child before insertion into the population
/// Currently there is a 20% chance of mutation with a 50/50 chance to increase or decrease the coordinate values
/// </summary>
/// <param name="child">The newly created chromosome to mutate before insertion into the population</param>
void GANodePlacement::Mutate(Chromosome& child)
{
    int xGene = 0;
    xGene = RandomRange(0, 100);

    if (xGene <= 20)
    {
        if (xGene % 2 == 0)
        {
            child.mPositions.first += RandomRange(0, 20);
            child.mPositions.second += RandomRange(0, 20);
        }
        else
        {
            child.mPositions.first -= RandomRange(0, 20);
            child.mPositions.second -= RandomRange(0, 20);
        }           
    }
}

/// <summary>
/// Produces a random number from within a given range 
/// </summary>
/// <param name="minVal">The lowest values in the range</param>
/// <param name="maxVal">The highest value in the range</param>
/// <returns>An integer represent the random number generated</returns>
int GANodePlacement::RandomRange(int minVal, int maxVal)
{
    std::random_device rd;
    std::mt19937 generator(rd());
    std::uniform_int_distribution<int> distribution(minVal, maxVal);
    return distribution(generator);
}
